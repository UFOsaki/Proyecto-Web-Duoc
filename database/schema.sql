-- ============================================
-- Sharingan Comics — Schema SQL
-- Base de datos: Oracle Autonomous (via Wallet)
-- Usuario: ADMIN
-- ============================================

-- Tabla principal de usuarios
CREATE TABLE USUARIOS (
    ID_USUARIO   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME     VARCHAR2(80)  NOT NULL UNIQUE,
    EMAIL        VARCHAR2(120) NOT NULL UNIQUE,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    PHONE        VARCHAR2(30),
    ROLE         VARCHAR2(30)  DEFAULT 'CUSTOMER' NOT NULL,
    CREATED_AT   TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT   TIMESTAMP
);

-- Índices para búsquedas rápidas
CREATE INDEX IDX_USUARIOS_EMAIL    ON USUARIOS(EMAIL);
CREATE INDEX IDX_USUARIOS_USERNAME ON USUARIOS(USERNAME);

-- Comentarios de tabla
COMMENT ON TABLE  USUARIOS IS 'Usuarios registrados en Sharingan Comics';
COMMENT ON COLUMN USUARIOS.PASSWORD_HASH IS 'Hash BCrypt — nunca guardar contraseña plana';
COMMENT ON COLUMN USUARIOS.ROLE IS 'Roles: CUSTOMER, ADMIN';

-- ============================================
-- Tablas de Pagos y Órdenes
-- ============================================

CREATE TABLE ORDENES (
    ID_ORDEN NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_USUARIO NUMBER NOT NULL,
    EXTERNAL_REFERENCE VARCHAR2(120) NOT NULL UNIQUE,
    BUYER_EMAIL VARCHAR2(120) NOT NULL,
    TOTAL NUMBER(12,2) NOT NULL,
    STATUS VARCHAR2(40) DEFAULT 'CREATED' NOT NULL,
    MP_PREFERENCE_ID VARCHAR2(120),
    MP_PAYMENT_ID VARCHAR2(120),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT FK_ORDEN_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO)
);

CREATE TABLE ORDEN_ITEMS (
    ID_ITEM NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_ORDEN NUMBER NOT NULL,
    PRODUCT_CODE VARCHAR2(80) NOT NULL,
    TITLE VARCHAR2(200) NOT NULL,
    DESCRIPTION VARCHAR2(500),
    QUANTITY NUMBER NOT NULL,
    UNIT_PRICE NUMBER(12,2) NOT NULL,
    SUBTOTAL NUMBER(12,2) NOT NULL,
    CONSTRAINT FK_ITEM_ORDEN FOREIGN KEY (ID_ORDEN)
        REFERENCES ORDENES(ID_ORDEN)
);

CREATE TABLE PAGOS_MP (
    ID_PAGO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_ORDEN NUMBER,
    MP_PAYMENT_ID VARCHAR2(120),
    MP_STATUS VARCHAR2(80),
    MP_STATUS_DETAIL VARCHAR2(120),
    MP_PAYMENT_TYPE VARCHAR2(80),
    RAW_NOTIFICATION CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_PAGO_ORDEN FOREIGN KEY (ID_ORDEN)
        REFERENCES ORDENES(ID_ORDEN)
);
